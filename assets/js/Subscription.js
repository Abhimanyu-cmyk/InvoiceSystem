(function () {
  class Subscription {
    constructor(data) {
      Object.assign(this, data || {});
      this.status = this.status || "active";
    }

    generateInvoice(nextInvoiceNumber, demoDate) {
      const dueOn = this.cycle === "Weekly" ? "2026-04-19" : this.cycle === "Monthly" ? "2026-05-10" : "2026-09-01";
      return {
        id: window.BillingUtils.makeId("inv"),
        number: "BIM-" + nextInvoiceNumber,
        title: this.name,
        customerId: this.customerId,
        currency: this.currency,
        issuedOn: demoDate,
        dueOn: dueOn,
        status: "scheduled",
        taxRatePct: this.currency === "INR" ? 18 : 0,
        discountCents: 0,
        scheduleType: "recurring",
        subscriptionId: this.id,
        accentColor: this.currency === "INR" ? "#0b6a6f" : "#2c6e63",
        logoText: this.name,
        notes: "Auto-generated by the recurring billing simulator.",
        lineItems: [{ description: this.name + " cycle", qty: 1, unitPriceCents: this.amountCents }]
      };
    }

    toJSON() {
      return {
        id: this.id,
        customerId: this.customerId,
        name: this.name,
        amountCents: this.amountCents,
        currency: this.currency,
        cycle: this.cycle,
        nextRun: this.nextRun,
        lastRun: this.lastRun,
        retryPolicy: this.retryPolicy,
        status: this.status
      };
    }

    static from(data) {
      return data instanceof Subscription ? data : new Subscription(data);
    }
  }

  window.BillingSubscription = Subscription;
})();
